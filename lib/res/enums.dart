enum AnimationType {
  /// Animates on entrance
  animateIn,
  /// Animates on exit
  animateOut,
  /// Animates both on entrance and on exit
  animateInAndOut
}

enum AnimationScrollDirection {
  /// User scrolling to the [forward] direction
  forward,
  /// User scrolling to the [reverse] direction
  reverse,
}
